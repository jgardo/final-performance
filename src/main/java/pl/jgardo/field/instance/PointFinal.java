package pl.jgardo.field.instance;

public class PointFinal {
    private final double x;
    private final double y;

    public PointFinal(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
